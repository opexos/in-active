CREATE FUNCTION PM_PORT_GET_CLIENTS_NAME(IN P_ID INTEGER) RETURNS D_VC_LONG
READS SQL DATA
BEGIN ATOMIC

DECLARE L_RES D_VC_LONG;

FOR 
		SELECT NAME FROM PM_PORT_CLIENTS WHERE PM_PORT_ID = P_ID ORDER BY IP
DO
		
  SET L_RES = L_RES || CASEWHEN(L_RES IS NULL, NULL, ';') || NAME;

END FOR;


RETURN L_RES;

END


DROP FUNCTION PM_PORT_GET_CLIENTS_NAME


