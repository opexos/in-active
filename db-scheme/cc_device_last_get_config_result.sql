CREATE FUNCTION CC_DEVICE_LAST_GET_CONFIG_RESULT (P_CC_DEVICE_ID INT)
  RETURNS D_GET_CONFIG_RESULT
  READS SQL DATA
  RETURN (SELECT TOP 1 RESULT
            FROM CC_LOG
           WHERE CC_DEVICE_ID = P_CC_DEVICE_ID
           ORDER BY LOG_DATE DESC)
             



DROP FUNCTION CC_DEVICE_LAST_GET_CONFIG_RESULT







