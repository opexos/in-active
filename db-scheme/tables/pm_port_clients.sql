CREATE TABLE PM_PORT_CLIENTS (
  ID INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1) NOT NULL,
  PM_PORT_ID INT NOT NULL,
  MAC D_VC,
  IP D_VC,
  NAME D_VC,
  VLAN INT,
	CISCO BOOLEAN NOT NULL,  
  FIRST_DETECT TIMESTAMP NOT NULL,
  LAST_DETECT TIMESTAMP NOT NULL,
  ARCHIVE BOOLEAN NOT NULL,
  VOICE BOOLEAN NOT NULL,
  
  CONSTRAINT PM_PORT_CLIENTS_PK PRIMARY KEY (ID),
  CONSTRAINT PM_PORT_CLIENTS_REF_PM_PORTS FOREIGN KEY (PM_PORT_ID) REFERENCES PM_PORTS(ID) ON DELETE CASCADE
)

