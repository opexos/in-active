CREATE TABLE CC_DEVICES (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) NOT NULL, 
  OBJECT_ID INTEGER NOT NULL,
  DEVICE_ID INTEGER NOT NULL,
  COMMENT D_VC_LONG,
  GET_CONFIG_INTERVAL D_VC NOT NULL,
  STORE_CONFIG_DAYS INTEGER NOT NULL,
  CONSTRAINT CC_DEVICES_PK PRIMARY KEY (ID),
  CONSTRAINT CC_DEVICES_REF_DEVICES FOREIGN KEY (DEVICE_ID) REFERENCES DEVICES(ID) ON DELETE CASCADE,
  CONSTRAINT CC_DEVICES_REF_OBJECTS FOREIGN KEY (OBJECT_ID) REFERENCES OBJECTS(ID) ON DELETE CASCADE,
  CONSTRAINT CC_DEVICES_UNIQUE_DEVICE_IN_OBJECT UNIQUE (DEVICE_ID,OBJECT_ID)
)

