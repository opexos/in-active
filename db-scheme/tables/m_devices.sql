CREATE TABLE M_DEVICES (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) NOT NULL, 
  HOST D_VC NOT NULL,
  LOGIN D_VC,
  PWD D_VC,
  ENABLE_PWD D_VC,
  NAME D_VC,
  COMMENT D_VC_LONG,
  DEVICE_TYPE_ID INTEGER NOT NULL,
  CONNECT_TYPE D_CONNECT_TYPE NOT NULL,
  M_OBJECT_ID INTEGER NOT NULL,
  LOCATION D_VC_LONG,
  CONSTRAINT M_DEVICES_PK PRIMARY KEY (ID),
  CONSTRAINT M_DEVICES_REF_DEVICE_TYPES FOREIGN KEY (DEVICE_TYPE_ID) REFERENCES DEVICE_TYPES(ID),
  CONSTRAINT M_DEVICES_REF_M_OBJECTS FOREIGN KEY (M_OBJECT_ID) REFERENCES M_OBJECTS(ID),
  CONSTRAINT M_DEVICES_UNIQUE_HOST_IN_OBJECT UNIQUE (HOST,M_OBJECT_ID)
)


