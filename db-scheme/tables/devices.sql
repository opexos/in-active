CREATE TABLE DEVICES (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) NOT NULL , 
  NAME D_VC NOT NULL,
  LOCATION D_VC_LONG,
  COMMENT D_VC_LONG,
  DEVICE_TYPE_ID INTEGER NOT NULL,
  HOST D_VC NOT NULL,

  LOGIN D_VC ,
  PWD D_VC ,
  ENABLE_PWD D_VC ,
  CONNECT_TYPE D_CONNECT_TYPE ,
  CONSOLE_PORT INTEGER ,
  
  SNMP_VERSION INTEGER ,
  READ_COMMUNITY D_VC ,
  WRITE_COMMUNITY D_VC ,
  SNMP_PORT INTEGER ,
  
  MAP_ID INTEGER NOT NULL,
  
  CONSTRAINT DEVICES_PK PRIMARY KEY (ID),
  CONSTRAINT DEVICES_REF_MAPS FOREIGN KEY (MAP_ID) REFERENCES MAPS(ID),
  CONSTRAINT DEVICES_REF_DEVICE_TYPES FOREIGN KEY (DEVICE_TYPE_ID) REFERENCES DEVICE_TYPES(ID),
  CONSTRAINT DEVICES_UNIQUE_NAME_IN_MAP UNIQUE (NAME, MAP_ID)
)

DROP TABLE DEVICES






