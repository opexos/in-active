CREATE TABLE PM_PORTS (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) NOT NULL, 
  PM_DEVICE_ID INT NOT NULL,
	PORT D_VC NOT NULL,
  TRUNK BOOLEAN DEFAULT FALSE NOT NULL,
  COMMENT D_VC_LONG,
  TELCO D_VC,
  PATCH D_VC,
  LOCATION D_VC_LONG,
  OFFLINE TIMESTAMP,
  VLAN INT,
  ADMIN_STATUS INT,
 
  CONSTRAINT PM_PORTS_PK PRIMARY KEY (ID),
  CONSTRAINT PM_PORTS_REF_PM_DEVICES FOREIGN KEY (PM_DEVICE_ID) REFERENCES PM_DEVICES(ID) ON DELETE CASCADE,  
  CONSTRAINT PM_PORTS_UNIQUE_PORT_IN_DEVICE UNIQUE (PORT, PM_DEVICE_ID)
)



