CREATE TABLE PM_VLANS (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) NOT NULL, 
  OBJECT_ID INT NOT NULL,
  VLAN INT NOT NULL,
  NAME D_VC,
  NET D_VC,
  VOICE BOOLEAN DEFAULT FALSE NOT NULL,
 
  CONSTRAINT PM_VLANS_PK PRIMARY KEY (ID),
  CONSTRAINT PM_VLANS_UNIQUE_VLAN_IN_OBJECT UNIQUE (OBJECT_ID,VLAN),
  CONSTRAINT PM_VLANS_REF_OBJECTS FOREIGN KEY (OBJECT_ID) REFERENCES OBJECTS(ID) ON DELETE CASCADE
)

