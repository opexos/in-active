package com.app.shared.exceptions;

public class ExIncompatibleScript extends Exception {

	public ExIncompatibleScript() {
		super();
	}

	public ExIncompatibleScript(String message) {
		super(message);
	}

	public ExIncompatibleScript(Throwable cause) {
		super(cause);
	}

	public ExIncompatibleScript(String message, Throwable cause) {
		super(message, cause);
	}

}
